int	ft_str_is_alpha(char *str)
{
	int	is_alpha;

	is_alpha = 1;
	while (*str != '\0')
	{
		if ((*str < 65 || *str > 90) && (*str < 97 || *str > 122) && *str != 0)
			is_alpha = 0;
		str++;
	}
	return (is_alpha);
}
